            <p> Ескерту. 25-1-баптың тақырыбы жаңа редакцияда – ҚР 24.11.2021 № 75-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңымен.</p>

