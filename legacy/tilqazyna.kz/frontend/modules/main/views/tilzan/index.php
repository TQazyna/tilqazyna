            <p> Ескерту. 26-бап алынып тасталды - Қазақстан Республикасының 2004.12.20. № 13 (2005 жылғы 1 қаңтардан бастап күшіне енеді) Заңымен.</p>

            <p>6 тарау. Тiлдi шет елдермен және халықаралық ұйымдармен<br>
            қатынастарда пайдалану<br>
            27-бап. Тiл - халықаралық қызметте</p>

            <p> Қазақстан Республикасының дипломатиялық өкiлдiктерi мен Қазақстан Республикасының халықаралық ұйымдар жанындағы өкiлдiктерiнiң қызметi мемлекеттiк тiлде, қажет болған жағдайда басқа да тiлдердi пайдаланып жүзеге асырылады.</p>

            <p> Қазақстан Республикасының шет мемлекеттермен екіжақты халықаралық шарттары Қазақстан Республикасының мемлекеттік тілінде және тараптардың келісуі бойынша өзге де тілдерде жасалады.</p>

            <p> Қазақстан Республикасының халықаралық ұйымдармен екіжақты халықаралық шарттары және Қазақстан Республикасының көпжақты халықаралық шарттары келіссөздерге қатысушы тараптардың келісуі бойынша айқындалған тілдерде жасалады.</p>

            <p> Қазақстан Республикасында басқа мемлекеттер өкiлдерiмен өткiзiлетiн ресми қабылдаулар мен өзге де шаралар басқа тiлдерге аударылып, мемлекеттiк тiлде жүргiзіледi.</p>

