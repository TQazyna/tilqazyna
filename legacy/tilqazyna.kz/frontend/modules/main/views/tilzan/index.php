            <h1>Қазақстан Республикасындағы тiл туралы заң</h1>
                <p> МАЗМҰНЫ</p>
                <p> Ескерту. Мәтiнде "тарау" деген сөздiң алдындағы "I - VI" деген цифрлар тиiсiнше "1 - 6" деген цифрлармен ауыстырылды - Қазақстан Республикасының 2004.12.20. № 13 (2005 жылғы 1 қаңтардан бастап күшіне енеді) Заңымен.<br>
                Осы Заң Қазақстан Республикасында тiлдердiң қолданылуының құқықтық негiздерiн, мемлекеттiң оларды оқып-үйрену мен дамыту үшiн жағдай жасау жөнiндегi мiндеттерiн белгiлейдi, Қазақстан Республикасында қолданылатын барлық тiлге бiрдей құрметпен қарауды қамтамасыз етедi.</p>

            <p>1 тарау. Жалпы ережелер<br>
            1-бап. Осы Заңда пайдаланылатын негізгі ұғымдар</p>

            <p> Осы Заңда мынадай негізгі ұғымдар пайдаланылады:</p>

            <p> 1) диаспора – өзiнiң тарихи шығу тегiнен тысқары елде тұрып жатқан халықтың бiр бөлiгi (этностық қауымдастық);</p>

            <p> 1-1) мәтіннің теңтүпнұсқалы аудармасы – түпнұсқаның мағынасы мен мазмұнын өзгеріссіз сақтайтын мәтіннің басқа тілге аудармасы;</p>

            <p> 2) ономастика – тiл бiлiмiнiң жалқы есiмдердi, олардың пайда болуы мен өзгеруiнiң тарихын зерттейтiн бөлiмi;</p>

            <p> 3) ономастика комиссиясы – Қазақстан Республикасының аумағындағы әкiмшiлiк-аумақтық бiрлiктерге, елді мекендердің құрамдас бөлiктерiне, әуежайларға, порттарға, теміржол вокзалдарына, теміржол стансаларына, метрополитен стансаларына, автовокзалдарға, автостансаларға, физикалық-географиялық және мемлекет меншігіндегі басқа да объектілерге атау беру және оларды қайта атау, олардың атауларының транскрипциясын нақтылау мен өзгерту және мемлекеттік заңды тұлғаларға, мемлекет қатысатын заңды тұлғаларға жеке адамдардың есімдерін беру, сондай-ақ Қазақстан Республикасының тарихи-мәдени мұрасының құрамдас бөлiгi ретiнде тарихи атауларды қалпына келтiру және сақтау бойынша бiрыңғай көзқарасты қалыптастыру жөнiндегi ұсыныстарды әзiрлейтiн консультациялық-кеңесшi орган;</p>

            <p> 3-1) ономастика саласындағы уәкілетті орган – ономастика саласындағы басшылықты және салааралық үйлестіруді жүзеге асыратын орталық атқарушы орган;</p>

            <p> 4) орфография – дұрыс жазу ережесi, сөйленген сөздi (сөздер мен грамматикалық тұлғаларды) жазбаша беру тәсiлдерiнiң бiрiздiлiгiн белгiлейтiн қағидалар жүйесi;</p>

            <p> 5) Республикалық терминология комиссиясы – Қазақстан Республикасы Үкіметінің жанындағы, экономиканың, ғылымның, техниканың және мәдениеттiң барлық салалары бойынша қазақ тiлiнiң терминологиялық лексикасы саласындағы ұсыныстарды әзiрлейтiн консультациялық-кеңесшi орган;</p>

            <p> 6) топонимика – ономастиканың географиялық объектiлердiң атауларын, олардың пайда болуының, өзгеруінің және қолданылуының заңдылықтарын зерттейтiн бөлiмi;</p>

            <p> 7) алып тасталды - ҚР 29.12.2021 № 94-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңымен;<br>
            8) тілдерді дамыту саласындағы уәкілетті орган – тілдерді дамыту саласындағы басшылықты және салааралық үйлестіруді жүзеге асыратын орталық атқарушы орган.</p>

            <p> Ескерту. 1-бап жаңа редакцияда - ҚР 21.01.2013 № 72-V (алғашқы ресми жарияланғанынан кейін үш ай өткен соң қолданысқа енгізiледi); өзгеріс енгізілді – ҚР 30.04.2021 № 34-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі); 24.11.2021 № 75-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі); 29.12.2021 № 94-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі Заңдарымен.<br>
            2-бап. Осы Заңның реттейтiн мәселесi</p>

            <p> Мемлекеттiк, мемлекеттiк емес ұйымдар мен жергiлiктi өзiн-өзi басқару органдарының қызметiнде тiлдердi қолдануға байланысты туындайтын қоғамдық қатынастар осы Заң реттейтiн мәселе болып табылады.</p>

            <p> Осы Заң жеке адамдар арасындағы қатынастарда және дiни бiрлестiктерде тiлдердiң қолданылуын реттемейдi.</p>

            <p>3-бап. Қазақстан Республикасының тiл туралы заңнамасы</p>

            <p> Ескерту. 3-баптың тақырыбы жаңа редакцияда - ҚР 29.12.2021 № 94-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңымен.</p>

            <p> Қазақстан Республикасының тiл туралы заңнамасы Қазақстан Республикасының Конституциясына негiзделедi, осы Заңнан, тiлдердi қолдануға және дамытуға қатысты Қазақстан Республикасының өзге де нормативтiк құқықтық актiлерiнен тұрады.</p>

            <p> Қазақстан Республикасының тiл туралы заңнамасы Қазақстан Республикасының азаматтарына, Қазақстан Республикасында тұрақты тұратын шетелдiктерге және азаматтығы жоқ адамдарға қолданылады.</p>

            <p> Ескерту. 3-бапқа өзгеріс енгізілді - ҚР 29.12.2021 № 94-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңымен.<br>
            4-бап. Қазақстан Республикасының мемлекеттiк тiлi</p>

            <p> Қазақстан Республикасының мемлекеттiк тiлi - қазақ тiлi.</p>

            <p> Мемлекеттiк тiл - мемлекеттiң бүкiл аумағында қоғамдық қатынастардың барлық саласында қолданылатын мемлекеттiк басқару, заң шығару, сот iсiн жүргiзу және iс қағаздарын жүргiзу тiлi.</p>

            <p> Қазақстан халқын топтастырудың аса маңызды факторы болып табылатын мемлекеттiк тiлдi меңгеру - Қазақстан Республикасының әрбiр азаматының парызы.</p>

            <p> Үкiмет, өзге де мемлекеттiк, жергiлiктi өкiлдi және атқарушы органдар:</p>

            <p> Қазақстан Республикасында мемлекеттiк тiлдi барынша дамытуға, оның халықаралық беделiн нығайтуға;</p>

            <p> Қазақстан Республикасының барша азаматтарының мемлекеттiк тiлдi еркiн және тегiн меңгеруiне қажеттi барлық ұйымдастырушылық, материалдық-техникалық жағдайларды жасауға;</p>

            <p> қазақ диаспорасына ана тiлiн сақтауы және дамытуы үшiн көмек көрсетуге мiндеттi.</p>

            <p>5-бап. Орыс тiлiн қолдану</p>

            <p> Мемлекеттiк ұйымдарда және жергiлiктi өзiн-өзi басқару органдарында орыс тiлi ресми түрде қазақ тiлiмен тең қолданылады.</p>

            <p>6-бап. Мемлекеттiң тiл жөнiндегi қамқорлығы</p>

            <p> Қазақстан Республикасының азаматының ана тiлiн қолдануына, қарым-қатынас, тәрбие, оқу және шығармашылық тiлiн еркiн таңдауына құқығы бар.</p>

            <p> Мемлекет Қазақстан халқының тiлдерiн оқып-үйрену мен дамыту үшiн жағдай туғызу жөнiнде қамқорлық жасайды.</p>

            <p> Ұлттық топтар жинақты тұратын жерлерде iс-шаралар өткiзiлген кезде олардың тiлдерi пайдаланылуы мүмкiн.</p>

            <p>7-бап. Тiлдердiң қолданылуына кедергi келтiруге жол бермеу</p>

            <p> Қазақстан Республикасында тiлдiк белгiсi бойынша азаматтардың құқықтарын кемсiтуге жол берiлмейдi.</p>

            <p> Қазақстанда мемлекеттiк тiлдiң және басқа да тiлдердiң қолданылуына және оларды үйренуге кедергi келтiретiн лауазымды адамдардың iс-әрекеттерi Қазақстан Республикасының заңдарына сәйкес жауаптылыққа әкеп соқтырады.</p>

            <p>2 тарау. Тiл - мемлекеттiк және мемлекеттiк емес ұйымдар мен<br>
            жергiлiктi өзiн-өзi басқару органдарында<br>
            8-бап. Тiлдердiң қолданылуы</p>

            <p> Мемлекеттiк тiл Қазақстан Республикасы мемлекеттiк органдарының, ұйымдарының және жергiлiктi өзiн-өзi басқару органдарының жұмыс және iс қағаздарын жүргiзу тiлi болып табылады, орыс тiлi ресми түрде қазақ тiлiмен тең қолданылады.</p>

            <p> Мемлекеттiк емес ұйымдардың жұмысында мемлекеттiк тiл және қажет болған жағдайда басқа тiлдер қолданылады.</p>

            <p>9-бап. Мемлекеттiк органдар актiлерiнiң тiлi</p>

            <p> Мемлекеттiк органдардың актiлерi мемлекеттiк тiлде әзiрленiп, қабылданады, қажет болған жағдайда, мүмкiндiгiнше, басқа тiлдерге аударылуы қамтамасыз етiле отырып, оларды әзiрлеу орыс тiлiнде жүргiзiлуi мүмкiн.</p>

            <p>10-бап. Құжаттама жүргiзу тiлi</p>

            <p> Қазақстан Республикасының мемлекеттiк органдары жүйесiнде, ұйымдарында, меншiк нысанына қарамастан, статистикалық-есеп, қаржы және техникалық құжаттама жүргiзу мемлекеттiк тiлде және орыс тiлiнде қамтамасыз етiледi.</p>

            <p> Ескерту. 10-бапқа өзгерту енгізілді - ҚР 2006.06.05 № 146; 24.11.2015 № 422-V (01.01.2016 бастап қолданысқа енгізіледі) Заңдарымен.<br>
            11-бап. Азаматтардың өтiнiштерiне қайтарылатын жауап тiлi</p>

            <p> Мемлекеттiк және мемлекеттiк емес ұйымдардың азаматтардың өтiнiштерi мен басқа да құжаттарға қайтаратын жауаптары мемлекеттiк тiлде немесе өтiнiш жасалған тiлде берiледi.</p>

            <p>12-бап. Қарулы Күштер мен құқық қорғану органдарындағы тiл</p>

            <p> Қазақстан Республикасының Қарулы Күштерiнде, сондай-ақ әскери және әскерилендiрiлген құрамалардың барлық түрiнде, мемлекеттiк бақылау мен қадағалау, азаматтарды құқықтық қорғау ұйымдарында және құқық қорғау органдарында мемлекеттiк тiлдiң және орыс тiлiнiң қолданылуы қамтамасыз етiледi.</p>

            <p>13-бап. Сот iсiн жүргiзу тiлi</p>

            <p> Қазақстан Республикасында сот iсi мемлекеттiк тiлде жүргiзiледi, ал, қажет болған жағдайда, сот iсiн жүргiзуде орыс тiлi немесе басқа тiлдер мемлекеттiк тiлмен тең қолданылады.</p>

            <p>14-бап. Әкiмшiлiк құқық бұзушылық туралы iстердi жүргiзу тiлi</p>

            <p> Әкiмшiлiк құқық бұзушылық туралы iстер мемлекеттiк тiлде, ал қажет болған жағдайда, басқа да тiлдерде жүргiзiледi.</p>

            <p>15-бап. Мәмiлелер тiлi</p>

            <p> Қазақстан Республикасында жеке және заңды тұлғалардың жазбаша нысанда жасалатын барлық мәмiлелерi қажет болған жағдайда басқа тiлдердегi аудармасы қоса берiлiп, мемлекеттiк тiлде және орыс тiлiнде жазылады.</p>

            <p> Шетелдiк жеке және заңды тұлғалармен жазбаша нысанда жасалатын мәмiлелер мемлекеттiк тiлде және тараптар үшiн қолайлы тiлде жазылады.</p>

            <p> Ескерту. 15-бапқа өзгерту енгізілді - ҚР 2006.06.05 № 146; 24.11.2015 № 422-V (01.01.2016 бастап қолданысқа енгізіледі) Заңдарымен.<br>
            3 тарау. Тiл - бiлiм беру, ғылым, мәдениет және бұқаралық<br>
            ақпарат құралдары саласында<br>
            16-бап. Тiл - бiлiм беру саласында</p>

            <p> Қазақстан Республикасында мемлекеттiк тiлде, ал ұлт топтары жинақы тұратын жерлерде солардың тiлдерiнде жұмыс iстейтiн мектепке дейiнгi балалар ұйымдарын құру қамтамасыз етiледi.</p>

            <p> Балалар үйлерiнде және оларға теңестiрiлген ұйымдарда оқыту, тәрбие беру тiлiн жергiлiктi атқарушы органдар олардағы балалардың ұлттық құрамын ескере отырып белгiлейдi.</p>

            <p> Қазақстан Республикасы бастауыш, негізгі орта, жалпы орта, техникалық және кәсіптік, орта білімнен кейінгі, жоғары және жоғары оқу орнынан кейінгі бiлiмдi мемлекеттiк тiлде, орыс тiлiнде, ал қажетiне қарай және мүмкiндiгi болған жағдайда басқа да тiлдерде алуды қамтамасыз етедi. Білім беру ұйымдарында мемлекеттiк тiл мен орыс тiлi мiндеттi оқу пәнi болып табылады және бiлiм туралы құжатқа енгiзiлетiн пәндер тiзбесiне кiредi.</p>

            <p> Ескерту. 16-бапқа өзгерту енгізілді - Қазақстан Республикасының 2007.07.27. № 320 (қолданысқа енгізілу тәртібін 2-баптан қараңыз) Заңымен.<br>
            17-бап. Тiл - ғылым және мәдениет саласында</p>

            <p> Қазақстан Республикасында ғылым саласында, диссертацияларды ресiмдеу мен қорғауды қоса алғанда, мемлекеттiк тiл мен орыс тiлiнiң қолданылуы қамтамасыз етiледi.</p>

            <p> Мәдени шаралар мемлекеттiк тiлде және қажет болған жағдайда басқа да тiлдерде жүргiзiледi.</p>

            <p>18-бап. Баспасөз бен бұқаралық ақпарат құралдарының тiлi</p>

            <p> Қазақстан Республикасы баспа басылымдары мен бұқаралық ақпарат құралдарында мемлекеттiк тiлдiң, басқа да тiлдердiң қолданылуын қамтамасыз етедi.</p>

            <p> Қажеттi тiлдiк ортаны жасау және мемлекеттiк тiлдiң толыққанды қолданылуы мақсатында, олардың меншiк нысанына қарамастан, теле-, радиоарналар арқылы берiлетiн мемлекеттiк тiлдегi теле-, радиобағдарламалардың көлемi уақыт жағынан басқа тiлдердегi теле-, радиобағдарламалардың жиынтық көлемiнен кем болмауға тиiс.</p>

            <p> Ескерту. 18-бапқа өзгеріс енгізілді - ҚР 2012.01.18 № 546-IV (алғашқы ресми жарияланғанынан кейін күнтізбелік отыз күн өткен соң қолданысқа енгізіледі) Заңымен.<br>
            4 тарау. Тiл - елдi мекен атауларында, жалқы есiмдерде,<br>
            көрнекi ақпаратта<br>
            19-бап. Топонимикалық атауларды пайдалану тәртiбi</p>

            <p> Ескерту. 19-баптың тақырыбына өзгеріс енгізілді - ҚР 29.12.2021 № 94-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңымен.</p>

            <p> Әкімшілік-аумақтық бірліктердің, елді мекендердің құрамдас бөліктерінің, сондай-ақ басқа да физика-географиялық объектiлердiң дәстүрлi, тарихи қалыптасқан қазақша атаулары мемлекеттік тілде жазылады.</p>

            <p> Ескерту. 19-бапқа өзгеріс енгізілді - ҚР 21.01.2013 № 72-V (алғашқы ресми жарияланғанынан кейін үш ай өткен соң қолданысқа енгізiледi); 29.12.2021 № 94-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңдарымен.<br>
            20-бап. Кiсi есiмiн, әкесiнiң есiмiн және тегiн жазу</p>

            <p> Кiсi есiмiн, әкесiнiң есiмiн және тегiн ресми құжаттарда жазу Қазақстан Республикасының заңдары мен нормативтiк құқықтық актiлерiне сәйкес келуге тиiс.</p>

            <p>21-бап. Деректемелер мен көрнекi ақпарат тiлi</p>

            <p> Деректемелер мен көрнекі ақпараттың мәтіндері орфография нормалары және мәтіннің теңтүпнұсқалы аудармасы сақтала отырып жазылады.</p>

            <p> Мемлекеттiк органдардың мөрлерi мен мөртаңбаларының мәтiнiнде олардың атаулары мемлекеттiк тiлде жазылады.</p>

            <p> Меншiк нысанына қарамастан, ұйымдардың мөрлерiнiң, мөртабандарының мәтiнi мемлекеттiк тiлде және орыс тiлiнде жазылады.</p>

            <p> Мемлекеттік ұйымдардың бланкілері мемлекеттік тілде және орыс тілінде, қажет болған кезде басқа тілдерде де жазылады.</p>

            <p> Мемлекеттік емес ұйымдардың бланкілері мемлекеттік тілде, қажет болған кезде орыс тілінде және (немесе) басқа тілдерде де жазылады.</p>

            <p> Мемлекеттік ұйымдардың маңдайшалары мемлекеттік тілде және орыс тілінде, қажет болған кезде басқа тілдерде де орналастырылады.</p>

            <p> Мемлекеттік емес ұйымдардың маңдайшалары мемлекеттік тілде, қажет болған кезде орыс тілінде және (немесе) басқа тілдерде де орналастырылады. Қазақстан Республикасында қорғалатын, мемлекеттік емес ұйымдардың маңдайшаларында пайдаланылатын тауар белгілері өзгермеген түрінде жазылады.</p>

            <p> Егер Қазақстан Республикасы ратификациялаған халықаралық шарттарда өзгеше көзделмесе, жол белгілеріндегі жазбалар мемлекеттік тілде жазылады.</p>

            <p> Егер Қазақстан Республикасының заңнамалық актілерінде өзгеше көзделмесе, хабарландырулар, жарнама, прейскуранттар, баға көрсеткiштерi, ас мәзірлері, нұсқағыштар және басқа да көрнекi ақпарат мемлекеттік тілде, қажет болған кезде орыс тілінде және (немесе) басқа тiлдерде де орналастырылады.</p>

            <p> Қазақстанда өндiрiлетiн тауарлардың арнайы мәлiметтер көрсетiлген тауарлық жапсырмаларында (этикеткаларында), таңбаламаларында, нұсқаулықтарында мемлекеттiк тiлде және орыс тiлiнде қажеттi ақпарат болуға тиiс.</p>

            <p> Шетелде өндiрiлген тауарлардың арнайы мәлiметтер көрсетiлген тауарлық жапсырмалары (этикеткалары), таңбаламалары, нұсқаулықтары импорттаушы ұйымдардың қаражаты есебiнен мемлекеттiк тiлдегi және орыс тiлiндегi аудармасымен қамтамасыз етiледi.</p>

            <p> Деректемелер мен көрнекі ақпараттың барлық мәтiнi мынадай ретпен: мемлекеттiк тiлде - сол жағына немесе жоғарғы жағына, орыс тiлiнде он жағына немесе төменгi жағына орналасады, бiрдей өлшемдегi әрiптермен жазылады. Қажеттiгiне қарай деректемелер мен көрнекі ақпараттың мәтiндерi қосымша басқа да тiлдерге аударылуы мүмкiн. Бұл жағдайда қарiп өлшемi нормативтiк құқықтық актiлерде белгiленген талаптардан аспауға тиiс. Ауызша ақпарат, хабарландыру, жарнама мемлекеттiк тiлде, орыс және қажет болған жағдайда, басқа да тiлдерде берiледi.</p>

            <p> Ескерту. 21-бапқа өзгеріс енгізілді - ҚР 29.12.2021 № 94-VII (қолданысқа енгізілу тәртібін 2-баптан қараңыз) Заңымен.<br>
            22-бап. Тіл - байланыс саласында</p>

            <p> Қазақстан Республикасының шегінде байланыс саласында мемлекеттік тілдің және орыс тілінің қолданылуы қамтамасыз етіледі. Қазақстан Республикасының шегінен тыс жерлерге почта-телеграф жөнелтілімдері белгіленген халықаралық ережелерге сәйкес жүргізіледі.</p>

            <p> Ескерту. 22-бап жаңа редакцияда – ҚР 2008.11.21 № 89-IV (қолданысқа енгізілу тәртібін 2-баптан қараңыз) Заңымен.<br>
            5 тарау. Тiлдi құқықтық қорғау<br>
            23-бап. Тiлдi мемлекеттiк қорғау</p>

            <p> Қазақстан Республикасында мемлекеттiк тiл және барлық басқа тiлдер мемлекеттiң қорғауында болады. Мемлекеттiк органдар бұл тiлдердiң қолданылуы мен дамуына қажеттi жағдай жасайды.</p>

            <p> Тiлдiң дамуы мемлекеттiк тiлдiң басымдығын және iс қағаздарын жүргiзудi қазақ тiлiне кезең-кезеңмен көшiрудi көздейтiн Қазақстан Республикасы Мемлекеттiк жоспарлау жүйесінің құжаттарымен қамтамасыз етiледi.</p>

            <p> Мемлекеттiк тiлдi белгiлi бiр көлемде және бiлiктiлiк талаптарына сәйкес бiлуi қажет кәсiптердiң, мамандықтардың және лауазымдардың тiзбесi Қазақстан Республикасы заңдарымен белгіленедi.</p>

            <p> Ескерту. 23-бапқа өзгерістер енгізілді - ҚР 20.12.2004 № 13 (01.01.2005 бастап күшіне енеді); 21.01.2013 № 72-V (алғашқы ресми жарияланғанынан кейін үш ай өткен соң қолданысқа енгізiледi); 03.07.2013 № 124-V (алғашқы ресми жарияланғанынан кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңдарымен.<br>
            24-бап. Қазақстан Республикасының тiл туралы заңнамасын бұзғаны үшiн жауаптылық</p>

            <p> Қазақстан Республикасының тіл туралы заңнамасының бұзылуына кінәлі мемлекеттік органдардың бірінші басшылары не аппараттардың басшылары, сондай-ақ жеке және заңды тұлғалар Қазақстан Республикасының заңдарына сәйкес жауаптылықта болады.</p>

            <p> Лауазымды адамның мемлекеттiк тiлдi бiлмеу желеуiмен жеке және заңды тұлғалардың өтiнiштерiн қабылдаудан бас тартуы, мемлекеттiк тiлдің және басқа да тiлдердің қолданылу салаларында олардың пайдаланылуына кез келген кедергi келтiру, сондай-ақ деректемелер мен көрнекі ақпаратты орналастыру бойынша талаптарды бұзу Қазақстан Республикасының заңдарында көзделген жауаптылыққа әкеп соғады.</p>

            <p> Ескерту. 24-бап жаңа редакцияда - ҚР 21.01.2013 N 72-V (алғашқы ресми жарияланғанынан кейін үш ай өткен соң қолданысқа енгізiледi); өзгеріс енгізілді – ҚР 30.12.2020 № 393-VI (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңдарымен.<br>
            24-1-бап. Қазақстан Республикасы Үкіметінің құзыреті</p>

            <p> Ескерту. 24-1-бап алып тасталды – ҚР 24.11.2021 № 75-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңымен.</p>

            <p>24-2-бап. Ономастика саласындағы уәкілетті органның құзыреті</p>

            <p> Ономастика саласындағы уәкілетті орган:</p>

            <p> 1) республикалық ономастика комиссиясының қызметін қамтамасыз етеді;</p>

            <p> 2) ономастика комиссияларының қызметін үйлестіреді;</p>

            <p> 2-1) облыстық ономастика комиссиялары және республикалық маңызы бар қалалардың, астананың ономастика комиссиялары туралы үлгілік ережені әзірлейді және бекітеді;</p>

            <p> 3) осы Заңда, Қазақстан Республикасының өзге де заңдарында, Қазақстан Республикасы Президентінің және Қазақстан Республикасы Үкіметінің актілерінде көзделген өзге де өкілеттіктерді жүзеге асырады.</p>

            <p> Ескерту. 5-тарау 24-2-баппен толықтырылды – ҚР 30.04.2021 № 34-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі); өзгеріс енгізілді – ҚР 24.11.2021 № 75-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңдарымен.<br>
            25-бап. Тілдерді дамыту саласындағы уәкілетті органның құзыреті</p>

            <p> Ескерту. 25-баптың тақырыбы жаңа редакцияда – ҚР 30.04.2021 № 34-VII (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) Заңымен.</p>

            <p> Тілдерді дамыту саласындағы уәкілетті орган:</p>

            <p> 1) тiлдердi дамыту саласындағы бiрыңғай мемлекеттiк саясаттың iске асырылуын қамтамасыз етедi;</p>

            <p> 2) алып тасталды - ҚР 03.07.2013 № 124-V Заңымен (алғашқы ресми жарияланғанынан кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі);</p>

            <p> 3) орталық және облыстардың, республикалық маңызы бар қалалардың, астананың жергiлiктi атқарушы органдарында Қазақстан Республикасының тiл туралы заңдарының сақталуын бақылауды жүзеге асырады;</p>

