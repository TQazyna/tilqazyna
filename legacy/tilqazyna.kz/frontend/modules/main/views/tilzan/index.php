            <p> 3) бір әкімшілік-аумақтық бірліктің шегіндегі елді мекендерге, елді мекендердің құрамдас бөліктеріне бір атауды бір мәрте ғана беру;</p>

            <p> 4) жеке адамның есімімен аталған, ол берілген (өзгертілген) күннен бастап кемінде он жыл өткен соң берілген есімді қайта атау, оны өзгерту;</p>

            <p> 5) қаһармандық пен ерлік танытқан, мемлекет тәуелсіздігін нығайтуға ерен үлес қосқан тұлғалардың есімдерін беру жағдайларын қоспағанда, аса көрнекті мемлекет және қоғам қайраткерлерінің, ғылым, мәдениет қайраткерлерінің және Қазақстан Республикасы мен әлемдік қоғамдастық алдында еңбек сіңірген басқа да адамдардың жеке есімдерін қайтыс болған күнінен бастап кемінде бес жыл өткен соң беру болып табылады.</p>

