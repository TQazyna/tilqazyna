        <br><p><h2 class="title2_forum" style="text-align:center;">РЕСПУБЛИКАЛЫҚ ТІЛ ФОРУМЫ</h2></p><br>

        <p><strong>Мақсаты:</strong> Қазақстан Республикасының Тұңғыш Президенті-Елбасының «Болашаққа бағдар: рухани жаңғыру» бағдарламасын және Қазақстан Республикасы Президенті Қ.К.Тоқаевтың «Сындарлы қоғамдық диалог – Қазақстанның тұрақтылығы мен өркендеуінің негізі» Жолдауының, Қазақстан Республикасы Премьер-Министрінің 2018 жылғы 13 наурыздағы №27-Ө өкімімен бекітілген «Қазақ тілі әліпбиін латын графикасына 2025 жылға дейін кезең-кезеңімен көшіру жөніндегі іс-шаралар жоспарының» міндеттерін іске асыру.</p>

        <p>Форум барысында мемлекеттік тілдің қолданыс аясын кеңейту, жаһандану үдерісіндегі қазақ тілінің болашағы мен даму бағыттары, латын графикасына көшіру барысы пікір алаңдарында талқыланады.</p>

        <p>Өңірлердегі тілдік ахуалға мониторинг жасалып, әр өңірдегі өзекті мәселелердің шешу жолдары сарапталады.</p>

        <p>Ғылым мен білім, заң, бизнес, ақпараттық кеңістіктегі қазақ тілінің рөлі мен стратегиялық даму бағыттары сала мамандарының талқылауына түседі.</p>

        <p><strong>Қатысушылар:</strong> Форумға ҚР Парламенті Сенаты, Мәжілісі депутаттары, орталық атқарушы органдардағы тілдерді дамыту басқармаларының басшылары, облыстық, Нұр-Сұлтан, Алматы, Шымкент қалаларының тілдерді дамыту басқармаларының/тілдерді дамыту бөлімдерінің басшылары, өңірлік тілдерді оқыту орталықтарының директорлары, ҚР Үкіметі жанындағы Ұлттық комиссияның орфографиялық, әдістемелік, терминологиялық, сараптама-техникалық жұмыс топтарының мүшелері, Ұлттық компаниялардағы мемлекеттік тілді дамыту департаментінің басшылары, мемлекеттік тілдің қолданысына жауапты мамандар, Қазақстандағы дипломатиялық корпус өкілдері, бизнес қауымдастық өкілдері, тілтанушы ғалымдар, Қазақстан халқы ассамлеясы өкілдері, этномәдени орталықтардың тілдерді дамыту бөлімдерінің басшылары, мамандары, БАҚ өкілдері, блогерлер, журналистер, тәуелсіз сарапшылар келеді.</p>

        <p><strong>Форум аясында:</strong></p>

        <p>1. «Жаһандану және ұлттану үдерісіндегі қазақ тілінің өміршеңдігі» ғылыми-практикалық конференциясы өтеді.</p>

        <p>2. Республикалық орталық коммуникациялар орталығында конференция қарары мен нәтижелері бойынша брифинг ұйымдастырылады.</p>

        <p>3. Барлық облыстардың, Нұр-Сұлтан, Алматы, Шымкент қалаларындағы тілдерді дамыту басқармаларының тапсырысымен, ұйымдастыруымен атқарылған жұмыстар мен әзірленген өнімдердің көрмесі өтеді.</p>

        <p><strong>Күтілетін нәтиже</strong></p>

        <p>Форум өткізу нәтижесінде</p>

        <p>- қазақ тілінің ұлтаралық қатынас тілі болуындағы өміршеңдігі мен әлеуеті пайымдалады;</p>

        <p>- қазақ тілі әліпбиін латын графикасына 2025 жылға дейін кезең-кезеңімен көшіру үдерісі пысықталады;</p>

        <p>- өңірлер бойынша атқарылған жұмыстарға мониторинг жасалады;</p>

        <p>- келесі жылдар жоспарына өзгерістер мен толықтырулар енгізуге ұсыныстар әзірленеді;</p>

        <p>- өзекті проблемалар талқыланады, шешімін табу жолдары қарастырылады;</p>

        <p>- БАҚ, әлеуметтік желілер мен сайттар арқылы жалпы қауымға насихаттау шаралары атқарылады;</p>

        <p>- 2020-2025 мемлекеттік бағдарлама жобасына ұсыныстар пысықталады;</p>

        <p>- өңірлік тілдерді дамыту басқармалары мен тілдерді оқыту орталықтары өзара тәжірибе алмасады.</p>

