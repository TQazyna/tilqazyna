                    <p><a href="//qazlatyn.kz"><strong>Qazlatyn.kz</strong></a> – мәтіндерді кирилден латынға және төте жазуға онлайн түрде сәйкестендіретін көпфункционалды конвертер және еліміздегі латын графикасына көшу үдерісін сүйемелдейтін негізгі ұлттық портал.</p>

                    <p>
                        Сайттан қазақ әліпбиінің латын графикасына көшу барысы жайындағы барлық деректі таба аласыз. Емле-ереже, жаңа әліпбидегі әріптердің 
                        таңбалануы мен жазылуы, дұрыс дыбысталуын тыңдау секілді айдарлары бар. Бұл портал латын графикасын үйренуге арналған қосымшаларды 
                        жүктеу жолдарын да көрсетеді. Сонымен қатар Qazlatyn көпфункционалды конвертері электронды мәтінді кирилден латынға, төтеге және 
                        керісінше аудару қызметтерінжүзеге асырады. Пайдаланушыға ыңғайлы болу үшін дайын мәтінді көшіріп салу немесе арнайы бетте жаза отырып, 
                        ілеспе сәйкестендіруге де болады. Емле, құжат, сөздікқор сияқты тілге қатысты танымдық сайттардың орталықтандырылған іздеу жүйесі де 
                        Qazlatyn.kz - ұлттық порталының басты бетінде орналасқан. 
